//Write a program for 10th student result, give grade to the student based on percentage

import java.util.Scanner;
class ResultOf10std 
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter marks: ");
		float marks = sc.nextFloat();
		float per = (marks/600)*100;
		String result = (per>=75) ? ("A") : ( (per>=60) ? ("B") : ( (per>=35)?("c"):("Fail") ) );
		System.out.println(per+"% Grade: "+result);
	}
}